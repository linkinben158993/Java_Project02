DELIMITER $$
CREATE TRIGGER INSERT_SINHVIEN
BEFORE INSERT ON SINHVIEN
FOR EACH ROW
BEGIN
	IF NEW.PASSWORD_SINHVIEN IS NULL 
		THEN
			BEGIN
				SET NEW.PASSWORD_SINHVIEN := NEW.CMND_SINHVIEN;
			END;
	END IF;
END$$

